#include "libshader.h"

t_vec2f	pos_to_vec2f(t_vec2r pos, t_vec2f res)
{
	return ((t_vec2f) { (((float)pos.x / res.x) * 2.0 - 1.0),
					((float)pos.y / res.y) * 2.0 - 1.0 });
}
