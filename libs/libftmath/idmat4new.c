#include "libftmath.h"

t_mat4	idmat4new(void)
{
	t_mat4	idmat;

	idmat.x = vec4new(1.0, 0.0, 0.0, 0.0);
	idmat.y = vec4new(0.0, 1.0, 0.0, 0.0);
	idmat.z = vec4new(0.0, 0.0, 1.0, 0.0);
	idmat.w = vec4new(0.0, 0.0, 0.0, 1.0);
	return (idmat);
}
