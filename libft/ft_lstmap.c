#include "libft.h"

t_list			*ft_lstmap(t_list *lst, t_list *(*f)(t_list *elem))
{
	t_list	*new;

	new = NULL;
	while (lst)
	{
		ft_lstradd(&new, f(lst));
		lst = lst->next;
	}
	return (new);
}
