#include "libftmath.h"

t_vec4	cross_product(t_vec4 a, t_vec4 b)
{
	t_vec4	c;

	c.x = a.y * b.z - a.z * b.y;
	c.y = a.z * b.x - a.x * b.z;
	c.z = a.x * b.y - a.y * b.x;
	return (c);
}
